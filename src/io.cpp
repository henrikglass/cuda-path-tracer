#define PRECISION_8 255
#define PRECISION_16 65535

#include "io.h"

float ffmin(float a, float b) {
    return (a < b) ? a : b;
}

/*
 * Modified from: https://github.com/henrikglass/erodr/blob/master/src/io.c
 */
void save_ppm(
        const std::string& filepath,
        const Image& img
) {
    FILE *fp = fopen(filepath.c_str(), "w");

    // write "header
    fputs("P6\n", fp);    
    fputs("# Generated by cuda-path-tracer\n", fp);
    fprintf(fp, "%d %d\n", img.resolution.x, img.resolution.y);    
    fprintf(fp, "%d\n", PRECISION_8);    
    
    // write data.
    for (int i = 0; i < img.resolution.x * img.resolution.y; i++) {
        char color[3];
        color[0] = ffmin(0.0004f * img.buf[i].x, 1.99f) * PRECISION_8;
        color[1] = ffmin(0.0004f * img.buf[i].y, 1.99f) * PRECISION_8;
        color[2] = ffmin(0.0004f * img.buf[i].z, 1.99f) * PRECISION_8;
        //color[0] = 0.0004f * img.buf[i].x * PRECISION_8;
        //color[1] = 0.0004f * img.buf[i].y * PRECISION_8;
        //color[2] = 0.0004f * img.buf[i].z * PRECISION_8;
        fwrite(color, sizeof(char), 3, fp);
    }

    fclose(fp);

    return;
}